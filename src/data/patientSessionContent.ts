// Per-patient session history — gives each offline demo patient their own
// session dates, summaries and therapist notes instead of the shared index-based
// set. Used only when VITE_API_BASE_URL is unset. When a patient has an entry
// here, their session count, dates, summaries and insights come from it; anyone
// without an entry falls back to the neutral demo set in ./sessions +
// ./sessionDetail. Each list is ordered most-recent-first (index 0 = latest), to
// match how the session builders index a patient's history.
import type { TranscriptLine } from './sessionDetail';

export interface PatientSessionContent {
  /** Session date, DD.MM.YYYY (matches SESSION_DATES formatting). */
  date: string
  /** Full session summary — feeds the "סיכום הפגישה" card + history row. */
  summary: string
  /** Therapist clinical note — feeds the "תובנה" insight card. */
  insight: string
  /** Optional verbatim excerpt; falls back to the generic set when absent. */
  transcript?: TranscriptLine[]
}

